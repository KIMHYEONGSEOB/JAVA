package api.lang.math;

public class MathEx {

	public static void main(String[] args) {
		
		System.out.println(Math.random());
		
		System.out.println(Math.ceil(3.14)); //올림
		
		System.out.println(Math.floor(3.14)); //내림
		
		System.out.println(Math.round(3.14)); //반올림
		
		System.out.println(Math.abs(-5)); //절대값
		
		System.out.println(Math.max(5,3));//큰수
		
		System.out.println(Math.min(5, 3)); //작은수
		
		System.out.println(Math.sqrt(4)); //루트값
		
		
	}
}
